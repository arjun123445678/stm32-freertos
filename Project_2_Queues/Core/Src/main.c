/**
  * @brief  Function implementing the Sender1 thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartSender1 */
void StartSender1(void *argument)
{
  uint8_t txData = 1;
  for(;;)
  {
    printf("Sender1: putting value %d\r\n", txData);

    if (osMessageQueuePut(Queue1Handle, &txData, 0, 200) == osOK) {
      printf("Sender1: put OK\r\n");
    } else {
      printf("Sender1: put FAILED or timeout\r\n");
    }

    osDelay(1000);
  }
}

/* USER CODE BEGIN Header_StartReceiver */
/**
* @brief Function implementing the Receiver thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartReceiver */
void StartReceiver(void *argument)
{
  uint8_t rxData = 0;
  for(;;)
  {
    printf("Receiver: waiting for message\r\n");

    if (osMessageQueueGet(Queue1Handle, &rxData, NULL, osWaitForever) == osOK) {
      printf("Receiver: got value %d\r\n", rxData);
    } else {
      printf("Receiver: timeout waiting for message\r\n");
    }
  }
}

